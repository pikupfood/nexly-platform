export function t(key: string, lang: Lang = 'fr'): string {
  return translations[lang]?.[key] || translations['fr']?.[key] || key
}

// Hook per usare i18n nei componenti
export function useTranslations(lang: Lang) {
  return (key: string) => t(key, lang)
}

// Detect lingua del browser
export function detectBrowserLang(): Lang {
  if (typeof window === 'undefined') return 'fr'
  const browserLang = navigator.language?.slice(0, 2)
  const supported: Lang[] = ['fr', 'it', 'en', 'de', 'es']
  return supported.includes(browserLang as Lang) ? browserLang as Lang : 'fr'
}

// Salva lingua preferita
export function saveLang(lang: Lang) {
  if (typeof window !== 'undefined') localStorage.setItem('nexly_lang', lang)
}

export function loadLang(): Lang {
  if (typeof window === 'undefined') return 'fr'
  const saved = localStorage.getItem('nexly_lang') as Lang
  const supported: Lang[] = ['fr', 'it', 'en', 'de', 'es']
  return supported.includes(saved) ? saved : detectBrowserLang()
}
